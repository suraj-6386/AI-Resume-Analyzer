@app.route("/editor")
def editor():
    """
    Load the Smart Editor with the resume text and suggestions
    from the last analysis stored in the session.

    Passes to editor.html:
        raw_text   — the original extracted resume text
        result     — the full analysis dict (for suggestions, score, etc.)
    """
    result = session.get("result")
    if not result:
        flash("No analysis data found. Please upload a resume first.", "info")
        return redirect(url_for("index"))

    # Retrieve the stored raw text from the uploaded file path if possible;
    # otherwise reconstruct from the analysis result filename.
    raw_text_path = os.path.join(
        app.config["UPLOAD_FOLDER"],
        result.get("filename", "")
    )
    raw_text = ""
    if os.path.exists(raw_text_path):
        try:
            with open(raw_text_path, "r", encoding="utf-8", errors="ignore") as f:
                raw_text = f.read()
        except Exception:
            pass

    # Fallback: if the file was a PDF and reading it as txt fails,
    # re-run the extractor (it's cached-cheap since the file still exists).
    if not raw_text and raw_text_path.endswith(".pdf"):
        try:
            from core.analyzer import extract_text
            raw_text = extract_text(raw_text_path)
        except Exception:
            raw_text = "[Could not extract text from PDF — please paste your resume here.]"

    return render_template("editor.html", raw_text=raw_text, r=result)


@app.route("/download-pdf", methods=["POST"])
def download_pdf():
    """
    Accept the edited resume text as JSON, generate an editorial PDF,
    and return it as a file attachment.

    Request JSON: { "text": "<full resume text>" }
    Response:     PDF binary with Content-Disposition: attachment
    """
    data = request.get_json(silent=True)
    if not data or "text" not in data:
        return jsonify({"error": "No text provided."}), 400

    resume_text = data["text"].strip()
    if not resume_text:
        return jsonify({"error": "Resume text is empty."}), 400

    try:
        pdf_buffer = generate_pdf(resume_text)
    except Exception as e:
        return jsonify({"error": f"PDF generation failed: {str(e)}"}), 500

    return send_file(
        pdf_buffer,
        mimetype="application/pdf",
        as_attachment=True,
        download_name="resume_optimized.pdf",
    )


