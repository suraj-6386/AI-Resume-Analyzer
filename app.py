@app.route("/")
def index():
    """Landing page — upload form."""
    return render_template("upload.html")


@app.route("/analyze", methods=["POST"])
def analyze():
    """
    Handle the resume upload.

    Flow:
        1. Validate file presence and extension.
        2. Save securely to the uploads folder.
        3. Run the analysis pipeline.
        4. Persist a summary row to SQLite history.
        5. Store the full result in the session and redirect to /report.
    """
    if "resume" not in request.files:
        flash("No file part in the request.", "error")
        return redirect(url_for("index"))

    file = request.files["resume"]

    if file.filename == "":
        flash("No file selected.", "error")
        return redirect(url_for("index"))

    if not allowed_file(file.filename):
        flash("Unsupported file type. Please upload a PDF or TXT file.", "error")
        return redirect(url_for("index"))

    # Save file
    safe_name = secure_filename(file.filename)
    save_path = os.path.join(app.config["UPLOAD_FOLDER"], safe_name)
    file.save(save_path)

    # Run the full analysis pipeline
    try:
        result = run_analysis(save_path, safe_name)
    except Exception as e:
        flash(f"Analysis failed: {str(e)}", "error")
        return redirect(url_for("index"))

    # Persist to SQLite history
    record_upload(
        filename   = safe_name,
        score      = result["score"]["overall"],
        grade      = result["score"]["grade"],
        word_count = result["word_count"],
    )

    # Store in session (JSON-serialisable dict)
    session["result"] = result
    return redirect(url_for("report"))


@app.route("/report")
def report():
    """Display the deep-dive analysis report."""
    result = session.get("result")
    if not result:
        flash("No analysis data found. Please upload a resume.", "info")
        return redirect(url_for("index"))
    return render_template("report.html", r=result)


@app.route("/history")
def history():
    """Show upload history from SQLite."""
    with get_db() as conn:
        rows = conn.execute(
            "SELECT * FROM uploads ORDER BY id DESC LIMIT 50"
        ).fetchall()
    # Convert sqlite3.Row → plain dicts for template
    records = [dict(row) for row in rows]
    return render_template("history.html", records=records)


@app.route("/api/history")
def api_history():
    """JSON endpoint for history data (optional AJAX use)."""
    with get_db() as conn:
        rows = conn.execute(
            "SELECT * FROM uploads ORDER BY id DESC LIMIT 50"
        ).fetchall()
    return jsonify([dict(row) for row in rows])


