"""
app.py
======
Flask orchestrator for the Smart Resume Analyzer.

Routes:
  GET  /             → upload.html  (drag-and-drop landing page)
  POST /analyze      → runs analysis, saves to SQLite history, redirects to /report
  GET  /report       → report.html  (deep-dive results)
  GET  /history      → history.html (past uploads from SQLite)
  GET  /editor       → editor.html  (Smart Editor — Notion-style canvas)
  POST /download-pdf → accepts JSON {text}, returns PDF attachment

SQLite DB (resume_history.db):
  Table: uploads (id, filename, score, grade, word_count, timestamp)

Author: Senior Full-Stack Engineer (generated by Antigravity AI)
"""

import os
import io
import sqlite3
import json
from datetime import datetime
from flask import (
    Flask, render_template, request, redirect,
    url_for, session, flash, jsonify, send_file
)
from werkzeug.utils import secure_filename
from core.analyzer import run_analysis
from core.pdf_builder import generate_pdf

# ---------------------------------------------------------------------------
# App Configuration
# ---------------------------------------------------------------------------

app = Flask(__name__)
app.secret_key = "smart-resume-analyzer-secret-2024"  # Change in production

BASE_DIR     = os.path.dirname(os.path.abspath(__file__))
UPLOAD_FOLDER = os.path.join(BASE_DIR, "uploads")
DB_PATH       = os.path.join(BASE_DIR, "resume_history.db")

app.config["UPLOAD_FOLDER"] = UPLOAD_FOLDER
app.config["MAX_CONTENT_LENGTH"] = 5 * 1024 * 1024  # 5 MB limit

ALLOWED_EXTENSIONS = {"pdf", "txt"}

os.makedirs(UPLOAD_FOLDER, exist_ok=True)


# ---------------------------------------------------------------------------
# SQLite — History Database
# ---------------------------------------------------------------------------

def get_db():
    """Open a connection to the SQLite history database."""
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row  # enables dict-like access
    return conn


def init_db():
    """
    Create the uploads table if it does not already exist.
    Called once on startup.
    """
    with get_db() as conn:
        conn.execute("""
            CREATE TABLE IF NOT EXISTS uploads (
                id         INTEGER PRIMARY KEY AUTOINCREMENT,
                filename   TEXT    NOT NULL,
                score      INTEGER NOT NULL,
                grade      TEXT    NOT NULL,
                word_count INTEGER NOT NULL,
                timestamp  TEXT    NOT NULL
            )
        """)
        conn.commit()


def record_upload(filename: str, score: int, grade: str, word_count: int):
    """Insert a new analysis result row into the history table."""
    ts = datetime.now().strftime("%Y-%m-%d %H:%M")
    with get_db() as conn:
        conn.execute(
            "INSERT INTO uploads (filename, score, grade, word_count, timestamp) "
            "VALUES (?, ?, ?, ?, ?)",
            (filename, score, grade, word_count, ts)
        )
        conn.commit()


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def allowed_file(filename: str) -> bool:
    """Return True if the file extension is in the allowed set."""
    return "." in filename and filename.rsplit(".", 1)[1].lower() in ALLOWED_EXTENSIONS


